        public static string GetThreeLetterCodeFromTwoLetterCode(string code)
        {
            if (string.IsNullOrEmpty(code))
            {
                return default;
            }

            return CountriesThreeLetter.TryGetValue(code.Trim(), out var isoCode) ? isoCode : default;
        }

        public static string GetNumericCodeFromTwoLetterCode(string code)
        {
            if (string.IsNullOrEmpty(code))
            {
                return default;
            }

            return CountriesNumeric.TryGetValue(code.Trim(), out var numericCode) ? numericCode : default;
        }

        public static string GetTwoLetterCodeFromNumericCode(string numericCode)
        {
            return string.IsNullOrWhiteSpace(numericCode)
                ? default
                : CountriesNumeric?.FirstOrDefault(x => x.Value == numericCode.Trim()).Key;
        }

        public static string GetTwoLetterCodeFromThreeLetterCode(string code)
        {
            return string.IsNullOrWhiteSpace(code)
                ? default
                : CountriesThreeLetter?.FirstOrDefault(x => x.Value == code.Trim()).Key;
        }

        public static string GetNameFromTwoLetterCode(string code)
            => CountriesName.TryGetValue(code, out var name) ? name : string.Empty;

        public static string GetCurrencyCodeFromTwoLetterCode(string code)
            => CountriesCurrencyCode.TryGetValue(code, out var currencyCode) ? currencyCode : string.Empty;

        public static IEnumerable<string> GetAllTwoLetterCodes()
            => CountriesThreeLetter.Keys;

        public static IEnumerable<string> GetAllThreeLetterCodes()
            => CountriesThreeLetter.Values;

        public static IEnumerable<string> GetAllNumericCodes()
            => CountriesNumeric.Values;

