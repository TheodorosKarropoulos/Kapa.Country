        #region Methods

        public static string GetThreeLetterCodeFromTwoLetterCode(string code)
        {
            if (string.IsNullOrEmpty(code))
            {
                return default;
            }

            return _countriesThreeLetter.TryGetValue(code.Trim(), out var isoCode) ? isoCode : default;
        }

        public static string GetNumericCodeFromTwoLetterCode(string code)
        {
            if (string.IsNullOrEmpty(code))
            {
                return default;
            }

            return _countriesNumeric.TryGetValue(code.Trim(), out var numericCode) ? numericCode : default;
        }

        public static string GetTwoLetterCodeFromNumericCode(string numericCode)
        {
            if (string.IsNullOrWhiteSpace(numericCode))
            {
                return default;
            }

            return _countriesNumeric?.FirstOrDefault(x => x.Value == numericCode.Trim()).Key;
        }

        public static string GetTwoLetterCodeFromThreeLetterCode(string code)
        {
            if (string.IsNullOrWhiteSpace(code))
            {
                return default;
            }

            return _countriesThreeLetter?.FirstOrDefault(x => x.Value == code.Trim()).Key;
        }

