        private static IReadOnlyDictionary<string, string> _countriesThreeLetter =
            new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)
            {
                {AfghanistanTwoLetterCode, AfghanistanThreeLetterCode},
                {AlandIslandsTwoLetterCode, AlandIslandsThreeLetterCode},
                {GreeceTwoLetterCode, GreeceThreeLetterCode}
            };

        private static IReadOnlyDictionary<string, string> _countriesNumeric =
            new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)
            {
                {AfghanistanTwoLetterCode, AfghanistanNumericCode},
                {AlandIslandsTwoLetterCode, AlandIslandsNumericCode},
                {GreeceTwoLetterCode, GreeceNumericCode}
            };

